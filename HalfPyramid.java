class HalfPyramid { 
     public static void main(String args[]) { 
    int row = 5;  
              for(int lRow = 1 ; lRow <= row ; lRow++  ) {
                    for( int cCol = 1 ; cCol <= lRow ; cCol++  ) { 
                      System.out.print("*");
                    }
              System.out.println();
               } 
               System.out.println("Program terminated");
     }    
 } 
 